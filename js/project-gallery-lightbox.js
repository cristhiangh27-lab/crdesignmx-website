const MIN_ZOOM = 1;
const MAX_ZOOM = 2;
const BUTTON_ZOOM_STEP = 0.25;
const WHEEL_ZOOM_FACTOR = 1.08;

const state = {
  index: 0,
  zoom: 1,
  offsetX: 0,
  offsetY: 0,
  pointers: new Map(),
  isDragging: false,
  startX: 0,
  startY: 0,
  pinchStartDistance: 0,
  pinchStartZoom: 1,
  lastFocus: null,
};

let dialog;
let galleryObserver;
let enhanceTimer;
let currentProject;

function projectSlug() {
  return document.body.dataset.project || '';
}

function config() {
  return LIGHTBOX_PROJECTS[projectSlug()];
}

function getLang() {
  const lang = window.__i18n?.lang || localStorage.getItem('lang') || document.documentElement.lang || 'en';
  return LIGHTBOX_COPY[lang] ? lang : 'en';
}

function text() {
  return LIGHTBOX_COPY[getLang()];
}

function rootPath() {
  if (window.__SITE_ROOT__) return window.__SITE_ROOT__;
  const { pathname } = window.location;
  return pathname.includes('/projects/') && !pathname.endsWith('/projects.html') ? '../..' : '.';
}

function asset(path) {
  return new URL(`${rootPath()}/${path}`, window.location.href).toString();
}

function normalizeImageSet(project) {
  return project.images.map(([src, en, es, de]) => ({
    src,
    caption: { en, es, de },
  }));
}

function imageList() {
  const project = config();
  return project ? normalizeImageSet(project) : [];
}

function imageKey(value) {
  try {
    const pathname = new URL(value, window.location.href).pathname;
    return decodeURIComponent(pathname.split('/').pop() || '').toLowerCase();
  } catch (error) {
    return String(value).split('/').pop().toLowerCase();
  }
}

function findImageIndex(src, fallbackIndex = 0) {
  const key = imageKey(src);
  const index = imageList().findIndex((item) => imageKey(item.src) === key);
  return index >= 0 ? index : fallbackIndex;
}

function caption(index) {
  const lang = getLang();
  const item = imageList()[index];
  return item?.caption[lang] || item?.caption.en || '';
}

function pad(value) {
  return String(value).padStart(2, '0');
}

function getStage() {
  return dialog?.querySelector('.cr-lightbox__stage');
}

function getImage() {
  return dialog?.querySelector('.cr-lightbox__image');
}

function updateZoomButtons() {
  if (!dialog) return;
  const reset = dialog.querySelector('[data-zoom-reset]');
  const out = dialog.querySelector('[data-zoom-out]');
  const input = dialog.querySelector('[data-zoom-in]');
  if (reset) reset.textContent = `${Math.round(state.zoom * 100)}%`;
  if (out) out.disabled = state.zoom <= MIN_ZOOM + 0.01;
  if (input) input.disabled = state.zoom >= MAX_ZOOM - 0.01;
}

function applyTransform() {
  const image = getImage();
  const stage = getStage();
  if (!image || !stage) return;
  image.style.transform = `translate3d(${state.offsetX}px, ${state.offsetY}px, 0) scale(${state.zoom})`;
  stage.classList.toggle('is-zoomed', state.zoom > MIN_ZOOM + 0.01);
  updateZoomButtons();
}

function resetZoom() {
  state.zoom = MIN_ZOOM;
  state.offsetX = 0;
  state.offsetY = 0;
  applyTransform();
}

function setZoom(nextZoom, origin) {
  const previousZoom = state.zoom;
  const zoom = Math.min(MAX_ZOOM, Math.max(MIN_ZOOM, Number(nextZoom.toFixed(3))));
  if (Math.abs(zoom - previousZoom) < 0.001) {
    updateZoomButtons();
    return;
  }

  if (origin && getStage()) {
    const rect = getStage().getBoundingClientRect();
    const fromCenterX = origin.x - rect.left - rect.width / 2 - state.offsetX;
    const fromCenterY = origin.y - rect.top - rect.height / 2 - state.offsetY;
    const ratio = zoom / previousZoom;
    state.offsetX -= fromCenterX * (ratio - 1);
    state.offsetY -= fromCenterY * (ratio - 1);
  }

  state.zoom = zoom;
  if (state.zoom <= MIN_ZOOM + 0.01) {
    state.offsetX = 0;
    state.offsetY = 0;
  }
  applyTransform();
}

function updateLabels() {
  if (!dialog) return;
  const labels = text();
  dialog.setAttribute('aria-label', labels.open);
  dialog.querySelector('.cr-lightbox__close')?.setAttribute('aria-label', labels.close);
  dialog.querySelector('[data-lightbox-prev]')?.setAttribute('aria-label', labels.previous);
  dialog.querySelector('[data-lightbox-next]')?.setAttribute('aria-label', labels.next);
  dialog.querySelector('[data-zoom-out]')?.setAttribute('aria-label', labels.zoomOut);
  dialog.querySelector('[data-zoom-in]')?.setAttribute('aria-label', labels.zoomIn);
  dialog.querySelector('[data-zoom-reset]')?.setAttribute('aria-label', labels.resetZoom);
  dialog.querySelector('.cr-lightbox__tools')?.setAttribute('aria-label', labels.zoomControls);
}

function updateTriggerText() {
  const labels = text();
  document.querySelectorAll('.cr-lightbox-trigger').forEach((button) => {
    button.setAttribute('aria-label', labels.open);
    const visible = button.querySelector('.cr-lightbox-trigger__text');
    if (visible) visible.textContent = labels.open;
  });
}

function updateImage() {
  if (!dialog) return;
  const images = imageList();
  const project = config();
  const imageData = images[state.index];
  const image = getImage();
  const title = dialog.querySelector('.cr-lightbox__title');
  const rights = dialog.querySelector('.cr-lightbox__rights');
  const counter = dialog.querySelector('.cr-lightbox__counter');
  const currentCaption = caption(state.index);

  if (image && imageData) {
    image.src = asset(imageData.src);
    image.alt = currentCaption;
  }
  if (title) title.textContent = `${project.title} - ${currentCaption}`;
  if (rights) rights.textContent = text().rights;
  if (counter) counter.textContent = `${pad(state.index + 1)} / ${pad(images.length)}`;
  updateLabels();
  applyTransform();
}

function show(delta) {
  const total = imageList().length;
  if (!total) return;
  state.index = (state.index + delta + total) % total;
  resetZoom();
  updateImage();
}

function closeLightbox() {
  if (!dialog || dialog.hidden) return;
  dialog.classList.remove('is-open');
  document.body.classList.remove('cr-lightbox-open');
  document.removeEventListener('keydown', onKeydown);
  state.pointers.clear();
  window.setTimeout(() => {
    if (!dialog.classList.contains('is-open')) dialog.hidden = true;
  }, 180);
  if (state.lastFocus && typeof state.lastFocus.focus === 'function') {
    state.lastFocus.focus({ preventScroll: true });
  }
}

function openLightbox(index) {
  if (!dialog) dialog = createLightbox();
  state.index = index;
  state.lastFocus = document.activeElement;
  resetZoom();
  updateImage();
  dialog.hidden = false;
  document.body.classList.add('cr-lightbox-open');
  document.addEventListener('keydown', onKeydown);
  window.requestAnimationFrame(() => {
    dialog.classList.add('is-open');
    dialog.querySelector('.cr-lightbox__close')?.focus({ preventScroll: true });
  });
}

function onKeydown(event) {
  if (event.key === 'Escape') closeLightbox();
  if (event.key === 'ArrowLeft') show(-1);
  if (event.key === 'ArrowRight') show(1);
  if (event.key === '+' || event.key === '=') setZoom(state.zoom + BUTTON_ZOOM_STEP);
  if (event.key === '-') setZoom(state.zoom - BUTTON_ZOOM_STEP);
  if (event.key === '0') resetZoom();
}

function pointerDistance() {
  const points = [...state.pointers.values()];
  if (points.length < 2) return 0;
  return Math.hypot(points[0].x - points[1].x, points[0].y - points[1].y);
}

function pointerCenter() {
  const points = [...state.pointers.values()];
  if (points.length < 2) return null;
  return {
    x: (points[0].x + points[1].x) / 2,
    y: (points[0].y + points[1].y) / 2,
  };
}

function isZoomToolTarget(target) {
  return target instanceof Element && Boolean(target.closest('.cr-lightbox__tools'));
}

function bindStage(stage) {
  stage.addEventListener('wheel', (event) => {
    if (isZoomToolTarget(event.target)) return;
    event.preventDefault();
    const factor = event.deltaY < 0 ? WHEEL_ZOOM_FACTOR : 1 / WHEEL_ZOOM_FACTOR;
    setZoom(state.zoom * factor, { x: event.clientX, y: event.clientY });
  }, { passive: false });

  stage.addEventListener('dblclick', (event) => {
    if (isZoomToolTarget(event.target)) return;
    setZoom(state.zoom > MIN_ZOOM + 0.01 ? MIN_ZOOM : MAX_ZOOM, { x: event.clientX, y: event.clientY });
  });

  stage.addEventListener('pointerdown', (event) => {
    if (isZoomToolTarget(event.target)) return;
    state.pointers.set(event.pointerId, { x: event.clientX, y: event.clientY });
    stage.setPointerCapture(event.pointerId);

    if (state.pointers.size === 2) {
      state.isDragging = false;
      state.pinchStartDistance = pointerDistance();
      state.pinchStartZoom = state.zoom;
      return;
    }

    if (state.zoom > MIN_ZOOM + 0.01) {
      state.isDragging = true;
      state.startX = event.clientX - state.offsetX;
      state.startY = event.clientY - state.offsetY;
    }
  });

  stage.addEventListener('pointermove', (event) => {
    if (!state.pointers.has(event.pointerId)) return;
    state.pointers.set(event.pointerId, { x: event.clientX, y: event.clientY });

    if (state.pointers.size >= 2 && state.pinchStartDistance > 0) {
      event.preventDefault();
      const nextZoom = state.pinchStartZoom * (pointerDistance() / state.pinchStartDistance);
      setZoom(nextZoom, pointerCenter());
      return;
    }

    if (!state.isDragging || state.zoom <= MIN_ZOOM + 0.01) return;
    event.preventDefault();
    state.offsetX = event.clientX - state.startX;
    state.offsetY = event.clientY - state.startY;
    applyTransform();
  });

  const releasePointer = (event) => {
    state.pointers.delete(event.pointerId);
    state.isDragging = false;
    state.pinchStartDistance = 0;
    if (stage.hasPointerCapture(event.pointerId)) stage.releasePointerCapture(event.pointerId);
  };

  stage.addEventListener('pointerup', releasePointer);
  stage.addEventListener('pointercancel', releasePointer);
}

function createLightbox() {
  const element = document.createElement('div');
  element.className = 'cr-lightbox';
  element.hidden = true;
  element.setAttribute('role', 'dialog');
  element.setAttribute('aria-modal', 'true');
  element.innerHTML = `
    <div class="cr-lightbox__backdrop" data-lightbox-close></div>
    <button class="cr-lightbox__close" type="button" data-lightbox-close><span aria-hidden="true">x</span></button>
    <button class="cr-lightbox__nav cr-lightbox__nav--prev" type="button" data-lightbox-prev><span aria-hidden="true">&lt;</span></button>
    <figure class="cr-lightbox__figure">
      <div class="cr-lightbox__stage">
        <img class="cr-lightbox__image" src="" alt="" draggable="false">
        <div class="cr-lightbox__tools">
          <button type="button" data-zoom-out>-</button>
          <button type="button" data-zoom-reset>100%</button>
          <button type="button" data-zoom-in>+</button>
        </div>
      </div>
      <figcaption class="cr-lightbox__caption">
        <span class="cr-lightbox__counter"></span>
        <h3 class="cr-lightbox__title"></h3>
        <p class="cr-lightbox__rights"></p>
      </figcaption>
    </figure>
    <button class="cr-lightbox__nav cr-lightbox__nav--next" type="button" data-lightbox-next><span aria-hidden="true">&gt;</span></button>
  `;

  document.body.append(element);
  element.querySelectorAll('[data-lightbox-close]').forEach((node) => node.addEventListener('click', closeLightbox));
  element.querySelector('[data-lightbox-prev]')?.addEventListener('click', () => show(-1));
  element.querySelector('[data-lightbox-next]')?.addEventListener('click', () => show(1));
  element.querySelector('[data-zoom-out]')?.addEventListener('click', () => setZoom(state.zoom - BUTTON_ZOOM_STEP));
  element.querySelector('[data-zoom-in]')?.addEventListener('click', () => setZoom(state.zoom + BUTTON_ZOOM_STEP));
  element.querySelector('[data-zoom-reset]')?.addEventListener('click', resetZoom);

  const stage = element.querySelector('.cr-lightbox__stage');
  if (stage) bindStage(stage);
  return element;
}

function createTrigger(index, mode) {
  const button = document.createElement('button');
  button.type = 'button';
  button.className = `cr-lightbox-trigger cr-lightbox-trigger--${mode}`;
  button.dataset.lightboxMode = mode;
  if (Number.isInteger(index)) button.dataset.lightboxIndex = String(index);
  button.innerHTML = `
    <span class="cr-lightbox-trigger__icon" aria-hidden="true">+</span>
    <span class="cr-lightbox-trigger__text"></span>
  `;
  button.addEventListener('click', (event) => {
    event.preventDefault();
    event.stopPropagation();
    if (button.dataset.lightboxMode === 'feature') {
      const featureImage = document.querySelector('#project-gallery .project-gallery__feature img');
      openLightbox(findImageIndex(featureImage?.currentSrc || featureImage?.src || '', 0));
      return;
    }
    openLightbox(Number(button.dataset.lightboxIndex || 0));
  });
  return button;
}

function enhanceLomasGallery(gallery) {
  const feature = gallery.querySelector('.project-gallery__feature');
  const thumbs = gallery.querySelector('.project-gallery__thumbs');
  if (!feature || !thumbs) return false;

  feature.classList.add('cr-lightbox-host', 'cr-lightbox-host--feature');
  if (![...feature.children].some((child) => child.classList?.contains('cr-lightbox-trigger'))) {
    feature.append(createTrigger(null, 'feature'));
  }

  thumbs.querySelectorAll('.project-gallery__thumb').forEach((thumb, index) => {
    const existingShell = thumb.closest('.cr-lightbox-thumb-shell');
    const img = thumb.querySelector('img');
    const resolvedIndex = findImageIndex(img?.currentSrc || img?.src || '', index);
    if (existingShell) {
      existingShell.querySelector('.cr-lightbox-trigger')?.setAttribute('data-lightbox-index', String(resolvedIndex));
      return;
    }
    const shell = document.createElement('span');
    shell.className = 'cr-lightbox-thumb-shell cr-lightbox-host';
    thumb.parentNode.insertBefore(shell, thumb);
    shell.append(thumb, createTrigger(resolvedIndex, 'thumb'));
  });
  return true;
}

function enhanceStandardGallery(gallery) {
  const grid = gallery.querySelector('.grid');
  if (!grid || grid.classList.contains('project-gallery__showcase')) return false;
  grid.classList.add('cr-lightbox-grid');

  [...grid.children].forEach((child, index) => {
    if (child.classList?.contains('cr-lightbox-image-shell')) {
      const img = child.querySelector('img');
      child.querySelector('.cr-lightbox-trigger')?.setAttribute('data-lightbox-index', String(findImageIndex(img?.src || '', index)));
      return;
    }
    if (child.tagName !== 'IMG') return;

    const shell = document.createElement('figure');
    shell.className = 'cr-lightbox-image-shell cr-lightbox-host';
    child.parentNode.insertBefore(shell, child);
    shell.append(child, createTrigger(findImageIndex(child.currentSrc || child.src, index), 'image'));
  });
  return true;
}

function enhanceGallery() {
  if (!config()) return;
  const gallery = document.querySelector('#project-gallery');
  if (!gallery) return;
  const didEnhance = enhanceLomasGallery(gallery) || enhanceStandardGallery(gallery);
  if (didEnhance) updateTriggerText();
}

function scheduleEnhance(delay = 0) {
  window.clearTimeout(enhanceTimer);
  enhanceTimer = window.setTimeout(enhanceGallery, delay);
}

function scheduleEnhancePasses() {
  [120, 500, 1100, 1900, 3000].forEach((delay) => window.setTimeout(enhanceGallery, delay));
}

function observeGallery() {
  const gallery = document.querySelector('#project-gallery');
  if (!gallery || galleryObserver) return;
  galleryObserver = new MutationObserver(() => scheduleEnhance(40));
  galleryObserver.observe(gallery, { childList: true, subtree: true });
}

function initLightbox() {
  currentProject = config();
  if (!currentProject) return;
  if (!dialog) dialog = createLightbox();
  observeGallery();
  scheduleEnhance(120);
  scheduleEnhancePasses();
}

if (document.readyState === 'loading') {
  window.addEventListener('DOMContentLoaded', initLightbox);
} else {
  initLightbox();
}
window.addEventListener('load', () => scheduleEnhance(150));
window.addEventListener('projectgalleryrender', () => scheduleEnhance(60));
window.addEventListener('langchange', () => {
  updateTriggerText();
  updateImage();
  scheduleEnhance(260);
});

window.ProjectGalleryLightbox = {
  enhance: enhanceGallery,
  schedule: scheduleEnhance,
};
